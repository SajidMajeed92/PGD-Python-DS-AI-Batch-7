# for addition
def addition(x,y):
    return x+y

# for subtraction
def subtraction(x,y):
    return x-y

# for multiplacation
def multiplacation(x,y):
    return x*y

# for division
def division(x,y):
    return x/y

# for reminder
def reminder(x,y):
    return x%y

